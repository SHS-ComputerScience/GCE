# Integer to String Function
# ---------------------------------------------------------------------------------------------------------------------------------------

def int_to_str(n, base):
	convertStr = "0123456789ABCDEF"
	if n < base:
		return convertStr[n]
	else:
		return int_to_str(n//base,base) + convertStr [n%base]